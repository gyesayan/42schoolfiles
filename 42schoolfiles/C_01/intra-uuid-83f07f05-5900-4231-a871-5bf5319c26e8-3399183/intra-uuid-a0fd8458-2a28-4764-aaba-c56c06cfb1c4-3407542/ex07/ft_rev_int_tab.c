void	ft_rev_int_tab(int *tab, int size)
{
	int b;
	int a;

	a = 0;
	while (a < size / 2)
	{
		b = tab[a];
		tab[a] = tab[size - a - 1];
		tab[size - a - 1] = b;
		a++;
	}
}
